GRANT SELECT, INSERT, UPDATE ON PUBLIC.DOCUMENT_CHUNKS TO SRI_APP_SERVICE;
GRANT SELECT, INSERT ON PUBLIC.RETRIEVAL_VALIDATIONS TO SRI_APP_SERVICE;
GRANT SELECT, INSERT ON PUBLIC.DOCUMENT_FEEDBACK TO SRI_APP_SERVICE;
GRANT SELECT ON PUBLIC.DOCUMENTS_WEIGHTED TO SRI_APP_SERVICE;
