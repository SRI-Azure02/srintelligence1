const SRI_BLUE  = "2891DA";
const SRI_DARK  = "1C1A16";
const SRI_LIGHT = "F7F8FA";

async function exportToPptx(report: StoryReport, date: string) {
  const pptxgen = (await import("pptxgenjs")).default;
  const prs = new pptxgen();
  prs.layout = "LAYOUT_WIDE"; // 13.33 × 7.5 inches

  const addHeader = (slide: ReturnType<typeof prs.addSlide>, title: string) => {
    slide.addShape(prs.ShapeType.rect, {
      x: 0, y: 0, w: "100%", h: 0.62,
      fill: { color: SRI_BLUE },
      line: { color: SRI_BLUE, width: 0 },
    });
    slide.addText(title, {
      x: 0.45, y: 0.1, w: 12.4, h: 0.42,
      fontSize: 17, bold: true, color: "FFFFFF",
    });
  };

  // ── Slide 1: Title ───────────────────────────────────────────────────────
  const s1 = prs.addSlide();
  s1.background = { color: SRI_DARK };

  s1.addText("SRIntelligence™", {
    x: 0.5, y: 0.45, w: 12, h: 0.35,
    fontSize: 11, bold: true, color: SRI_BLUE, charSpacing: 2,
  });
  s1.addShape(prs.ShapeType.rect, {
    x: 0.5, y: 0.9, w: 1.2, h: 0.06,
    fill: { color: SRI_BLUE }, line: { color: SRI_BLUE, width: 0 },
  });
  s1.addText(report.title, {
    x: 0.5, y: 1.15, w: 11.5, h: 3.2,
    fontSize: 30, bold: true, color: "FFFFFF", valign: "top", wrap: true,
  });
  s1.addText("Executive Report", {
    x: 0.5, y: 4.5, w: 12, h: 0.4,
    fontSize: 14, color: "999999",
  });
  s1.addText(date, {
    x: 0.5, y: 7.1, w: 12, h: 0.3,
    fontSize: 9, color: "555555",
  });

  // ── Slide 2: Executive Summary ───────────────────────────────────────────
  const s2 = prs.addSlide();
  s2.background = { color: "FFFFFF" };
  addHeader(s2, "Executive Summary");
  s2.addText(report.executiveSummary, {
    x: 0.45, y: 0.82, w: 12.4, h: 6.3,
    fontSize: 11, color: SRI_DARK, valign: "top", wrap: true, paraSpaceAfter: 8,
  });

  // ── Slide 3: Key Findings ────────────────────────────────────────────────
  const s3 = prs.addSlide();
  s3.background = { color: "FFFFFF" };
  addHeader(s3, "Key Findings");
  report.keyFindings.forEach((finding, i) => {
    const y = 0.88 + i * 0.72;
    if (y > 7.0) return; // guard overflow
    s3.addShape(prs.ShapeType.ellipse, {
      x: 0.45, y: y + 0.02, w: 0.28, h: 0.28,
      fill: { color: SRI_BLUE }, line: { color: SRI_BLUE, width: 0 },
    });
    s3.addText(String(i + 1), {
      x: 0.45, y: y + 0.02, w: 0.28, h: 0.28,
      fontSize: 9, bold: true, color: "FFFFFF", align: "center", valign: "middle",
    });
    s3.addText(finding, {
      x: 0.85, y, w: 12.0, h: 0.62,
      fontSize: 11, color: SRI_DARK, valign: "middle", wrap: true,
    });
  });

  // ── Slides for each analysis section ────────────────────────────────────
  for (const section of report.sections) {
    const sN = prs.addSlide();
    sN.background = { color: "FFFFFF" };
    addHeader(sN, section.heading);
    sN.addText(section.body, {
      x: 0.45, y: 0.82, w: 12.4, h: 6.3,
      fontSize: 11, color: SRI_DARK, valign: "top", wrap: true, paraSpaceAfter: 8,
    });
  }

  // ── Recommendations slide ────────────────────────────────────────────────
  const sR = prs.addSlide();
  sR.background = { color: SRI_LIGHT };
  addHeader(sR, "Recommendations");
  report.recommendations.forEach((rec, i) => {
    const y = 0.88 + i * 1.02;
    if (y > 7.0) return;
    sR.addShape(prs.ShapeType.rect, {
      x: 0.45, y, w: 12.4, h: 0.86,
      fill: { color: "FFFFFF" },
      line: { color: SRI_BLUE, width: 1.5, dashType: "solid" },
      rectRadius: 4,
    });
    sR.addText(`${i + 1}.`, {
      x: 0.6, y: y + 0.06, w: 0.35, h: 0.72,
      fontSize: 13, bold: true, color: SRI_BLUE, valign: "middle",
    });
    sR.addText(rec, {
      x: 0.98, y: y + 0.06, w: 11.7, h: 0.72,
      fontSize: 11, color: SRI_DARK, valign: "middle", wrap: true,
    });
  });

  // ── Methodology slide ────────────────────────────────────────────────────
  const sM = prs.addSlide();
  sM.background = { color: "FFFFFF" };
  addHeader(sM, "Methodology & Agents");
  sM.addText(report.methodology, {
    x: 0.45, y: 0.82, w: 12.4, h: 4.5,
    fontSize: 11, color: SRI_DARK, valign: "top", wrap: true,
  });
  if (report.agentsUsed.length > 0) {
    sM.addText("Agents Used", {
      x: 0.45, y: 5.5, w: 12, h: 0.35,
      fontSize: 10, bold: true, color: "888888",
    });
    sM.addText(report.agentsUsed.join("  ·  "), {
      x: 0.45, y: 5.85, w: 12, h: 0.45,
      fontSize: 10, color: "555555",
    });
  }
  sM.addText(`Generated by SRIntelligence™  ·  ${date}`, {
    x: 0.45, y: 7.15, w: 12, h: 0.25,
    fontSize: 9, color: "AAAAAA",
  });

  const safeTitle = report.title.replace(/[^a-z0-9\s-]/gi, "").trim().replace(/\s+/g, "_");
  await prs.writeFile({ fileName: `${safeTitle}_Executive_Report.pptx` });
}

