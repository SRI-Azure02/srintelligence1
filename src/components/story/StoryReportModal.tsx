export default function StoryReportModal({ report, onClose }: Props) {
  const [exportingPptx, setExportingPptx] = useState(false);

  const generatedDate = new Date().toLocaleDateString("en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
  });

  const handleExportPDF = () => {
    const win = window.open("", "_blank");
    if (!win) { alert("Allow pop-ups to export PDF."); return; }
    win.document.write(buildPrintHTML(report, generatedDate));
    win.document.close();
  };

  const handleExportPptx = async () => {
    setExportingPptx(true);
    try {
      await exportToPptx(report, generatedDate);
    } catch (err) {
      console.error("PPTX export failed:", err);
      alert("PowerPoint export failed — please try PDF instead.");
    } finally {
      setExportingPptx(false);
    }
  };

  return (
    <div className="fixed inset-0 z-[100] flex items-center justify-center">
      {/* Backdrop */}
      <div className="absolute inset-0 bg-black/60" onClick={onClose} />

      {/* Modal panel */}
      <div
        className="relative z-10 flex flex-col rounded-2xl overflow-hidden shadow-2xl"
        style={{ width: "min(880px, 96vw)", height: "92vh", background: "#ffffff" }}
      >
        {/* ── Header bar ───────────────────────────────────────────────── */}
        <div
          className="flex items-center justify-between px-6 py-3.5 shrink-0"
          style={{ background: "#1C1A16", borderBottom: "1px solid rgba(255,255,255,0.08)" }}
        >
          <div className="flex items-center gap-3">
            {/* Brand icon */}
            <div
              className="flex items-center justify-center w-7 h-7 rounded-lg shrink-0"
              style={{ background: "linear-gradient(135deg, #2891DA 0%, #C8956A 100%)" }}
            >
              <BookOpen size={14} style={{ color: "white" }} />
            </div>
            <div>
              <p className="text-sm font-semibold" style={{ color: "white" }}>
                Executive Report
              </p>
              <p className="text-xs" style={{ color: "rgba(255,255,255,0.45)" }}>
                {generatedDate}
              </p>
            </div>
          </div>

          <div className="flex items-center gap-2">
            {/* PDF */}
            <button
              onClick={handleExportPDF}
              className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-semibold transition-opacity hover:opacity-80"
              style={{
                background: "rgba(255,255,255,0.1)",
                color: "white",
                border: "1px solid rgba(255,255,255,0.18)",
              }}
            >
              <Download size={12} />
              Export PDF
            </button>

            {/* PPTX */}
            <button
              onClick={handleExportPptx}
              disabled={exportingPptx}
              className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-semibold transition-opacity hover:opacity-85 disabled:opacity-50"
              style={{ background: "#2891DA", color: "white" }}
            >
              {exportingPptx ? (
                <><Loader2 size={12} className="animate-spin" />Building…</>
              ) : (
                <>
                  <svg width="12" height="12" viewBox="0 0 16 16" fill="none" style={{ flexShrink: 0 }}>
                    <rect x="1" y="1" width="14" height="14" rx="2" stroke="white" strokeWidth="1.5" />
                    <rect x="4" y="4" width="4" height="8" rx="1" fill="white" />
                    <rect x="10" y="4" width="2" height="2" rx="0.5" fill="white" />
                    <rect x="10" y="7" width="2" height="2" rx="0.5" fill="white" />
                    <rect x="10" y="10" width="2" height="2" rx="0.5" fill="white" />
                  </svg>
                  PowerPoint
                </>
              )}
            </button>

            {/* Close */}
            <button
              onClick={onClose}
              className="flex items-center justify-center w-8 h-8 rounded-lg transition-opacity hover:opacity-70"
              style={{ background: "rgba(255,255,255,0.08)", color: "rgba(255,255,255,0.7)" }}
            >
              <X size={15} />
            </button>
          </div>
        </div>

        {/* ── Scrollable report body ────────────────────────────────────── */}
        <div
          className="flex-1 overflow-y-auto px-12 py-10"
          style={{ background: "#fafafa" }}
        >
          {/* Report header */}
          <div className="mb-8 pb-6" style={{ borderBottom: "3px solid #2891DA" }}>
            <p
              className="text-xs font-bold tracking-widest uppercase mb-2"
              style={{ color: "#2891DA", letterSpacing: "0.1em" }}
            >
              SRIntelligence · Executive Report
            </p>
            <h1 className="text-3xl font-extrabold mb-2 leading-tight" style={{ color: "#1C1A16" }}>
              {report.title}
            </h1>
            <p className="text-sm" style={{ color: "#9CA3AF" }}>{generatedDate}</p>
          </div>

          {/* Executive Summary */}
          <Section title="Executive Summary">
            {report.executiveSummary.split(/\n{2,}/).map((p, i) => (
              <p key={i} className={`text-sm leading-relaxed ${i > 0 ? "mt-3" : ""}`} style={{ color: "#374151" }}>
                {p}
              </p>
            ))}
          </Section>

          {/* Key Findings */}
          <Section title="Key Findings">
            <ul className="flex flex-col gap-2.5">
              {report.keyFindings.map((finding, i) => (
                <li key={i} className="flex items-start gap-3">
                  <span
                    className="flex items-center justify-center text-xs font-bold shrink-0 rounded-full mt-0.5"
                    style={{ width: 20, height: 20, background: "#2891DA", color: "white" }}
                  >
                    {i + 1}
                  </span>
                  <span className="text-sm leading-relaxed" style={{ color: "#374151" }}>
                    {finding}
                  </span>
                </li>
              ))}
            </ul>
          </Section>

          {/* Analysis sections */}
          {report.sections.map((section, i) => (
            <Section key={i} title={section.heading}>
              {section.body.split(/\n{2,}/).map((p, j) => (
                <p key={j} className={`text-sm leading-relaxed ${j > 0 ? "mt-3" : ""}`} style={{ color: "#374151" }}>
                  {p}
                </p>
              ))}
            </Section>
          ))}

          {/* Recommendations */}
          <div
            className="mb-8 p-6 rounded-xl"
            style={{ background: "rgba(40,145,218,0.05)", border: "1px solid rgba(40,145,218,0.2)" }}
          >
            <h2 className="text-base font-bold mb-4" style={{ color: "#1C1A16" }}>
              Recommendations
            </h2>
            <ul className="flex flex-col gap-3">
              {report.recommendations.map((rec, i) => (
                <li key={i} className="flex items-start gap-3">
                  <span className="font-bold mt-0.5 shrink-0" style={{ color: "#2891DA", fontSize: 14 }}>
                    →
                  </span>
                  <span className="text-sm leading-relaxed" style={{ color: "#374151" }}>
                    {rec}
                  </span>
                </li>
              ))}
            </ul>
          </div>

          {/* Methodology */}
          <Section title="Methodology">
            <p className="text-sm leading-relaxed" style={{ color: "#374151" }}>
              {report.methodology}
            </p>
            {report.agentsUsed.length > 0 && (
              <div className="flex flex-wrap gap-2 mt-3">
                {report.agentsUsed.map((agent, i) => (
                  <span
                    key={i}
                    className="text-xs px-2.5 py-1 rounded-full"
                    style={{
                      background: "var(--bg-secondary)",
                      color: "var(--text-muted)",
                      border: "1px solid var(--border)",
                    }}
                  >
                    {agent}
                  </span>
                ))}
              </div>
            )}
          </Section>

          {/* Footer */}
          <div
            className="mt-8 pt-4 flex justify-between text-xs"
            style={{ borderTop: "1px solid var(--border)", color: "var(--text-muted)" }}
          >
            <span>Generated by SRIntelligence™</span>
            <span>{generatedDate}</span>
          </div>
        </div>
      </div>
    </div>
  );
}

// ---------------------------------------------------------------------------
// Section helper
// ---------------------------------------------------------------------------

function Section({ title, children }: { title: string; children: React.ReactNode }) {
  return (
    <div className="mb-8">
      <h2 className="text-base font-bold mb-3" style={{ color: "#1C1A16" }}>
        {title}
      </h2>
      {children}
    </div>
  );
}
